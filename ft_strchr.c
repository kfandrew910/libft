char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*ptr;

	i = 0;
	ptr = (char *)s;
	if (c >= 256)
		c = c % 256;
	while (s[i] != 0)
	{
		if (s[i] == c)
			return (ptr + i);
		i++;
	}
	if (c == 0)
		return (ptr + i);
	return (0);
}

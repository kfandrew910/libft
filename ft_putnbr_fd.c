#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	size_t	i;
	char	*ptr;

	ptr = ft_itoa(n);
	i = 0;
	while (ptr[i])
		ft_putchar_fd(ptr[i++], fd);
}
